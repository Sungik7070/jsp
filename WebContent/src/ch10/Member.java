package ch10;

public class Member {
	private String id;
	private String name;
	private String pwp;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPwp() {
		return pwp;
	}
	public void setPwp(String pwp) {
		this.pwp = pwp;
	}

}
